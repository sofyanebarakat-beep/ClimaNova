import fs from "node:fs";
import path from "node:path";
import crypto from "node:crypto";

const root = process.cwd();
const excludedDirs = new Set([".git", "node_modules", "ClimaNovaEnergie copy", "climavovaall"]);
const styleMap = new Map();

function listHtmlFiles(dir = root) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    if (excludedDirs.has(entry.name)) continue;

    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...listHtmlFiles(fullPath));
    } else if (entry.isFile() && entry.name.endsWith(".html")) {
      files.push(fullPath);
    }
  }

  return files;
}

function classNameFor(style) {
  const normalized = style.trim().replace(/\s+/g, " ");
  const hash = crypto.createHash("sha1").update(normalized).digest("hex").slice(0, 10);
  const className = `cn-style-${hash}`;
  styleMap.set(className, normalized);
  return className;
}

function addClass(tag, className) {
  if (/\sclass=(["'])/.test(tag)) {
    return tag.replace(/\sclass=(["'])(.*?)\1/, (match, quote, classes) => {
      if (classes.split(/\s+/).includes(className)) return match;
      return ` class=${quote}${classes} ${className}${quote}`;
    });
  }

  return tag.replace(/^<([^\s>]+)/, `<$1 class="${className}"`);
}

function extractStyles(html) {
  return html.replace(/<[^<>]+\sstyle="([^"]+)"[^<>]*>/g, (tag, style) => {
    const className = classNameFor(style);
    const withoutStyle = tag.replace(/\sstyle="[^"]+"/, "");
    return addClass(withoutStyle, className);
  });
}

for (const file of listHtmlFiles()) {
  const original = fs.readFileSync(file, "utf8");
  const next = extractStyles(original);

  if (next !== original) {
    fs.writeFileSync(file, next);
    console.log(`extracted styles ${path.relative(root, file)}`);
  }
}

const css = [
  "/* Generated by scripts/extract-inline-styles.mjs. Do not edit by hand. */",
  ...[...styleMap.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([className, style]) => `.${className} { ${style} }`),
  "",
].join("\n");

fs.writeFileSync(path.join(root, "css/pages/inline-generated.css"), css);
